    private void filterBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_filterBtnActionPerformed
        // TODO add your handling code here:
        boolean filter_parking, filter_3d, filter_both;
        filter_parking = parkingChkBox.isSelected();
        filter_3d = threeDChkBox.isSelected();
        filter_both = filter_3d && filter_parking;
        if (!filter_parking && !filter_3d) {
            JOptionPane.showMessageDialog(rootPane, "Please select at least one filter options.", "No Option Selected", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ArrayList<Object[]> tableData = getTableData();
        ArrayList<Object[]> filteredData = new ArrayList();

        tableData.forEach((x) -> {
            String parking = (String) x[4];
            String threeD = (String) x[5];
            if (filter_both) {
                if (parking.toLowerCase().equals("yes") && threeD.toLowerCase().equals("yes")) {
                    filteredData.add(x);
                }
            } else if (filter_parking) {
                if (parking.toLowerCase().equals("yes")) {
                    filteredData.add(x);
                }
            } else {
                if (threeD.toLowerCase().equals("yes")) {
                    filteredData.add(x);
                }
            }
        });

        // Converting the arraylist to an Iterator
        Iterator<Object[]> iter = filteredData.iterator();

        // Running the loop as long as the iterator has a next value to process.
        while (iter.hasNext()) {
            Object[] data = iter.next();
            String displayText = "Filtered Results (" + filteredData.size() + " Cinemas Found):\n";
            displayText += "Cinema ID: " + data[0] + "\n";
            displayText += "Cinema Name: " + data[1] + "\n";
            displayText += "Address: " + data[2] + "\n";
            displayText += "Contact: " + data[3] + "\n";
            displayText += "Parking: " + data[4] + "\n";
            displayText += "3D Support: " + data[5] + "\n";
            displayText += "Ticket Price in " + rateNames[currentCurrency] + ": " + data[6] + "\n";
            int option;// This stores what value the user has chosen.
            // Putting the input values according to the iterator's remaining components

            if (iter.hasNext()) {
                Object[] options = {"Close", "Next"};
                option = JOptionPane.showOptionDialog(rootPane, displayText,
                        "Filter Search Details", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE,
                        null, options, options[0]);
            } else {
                Object[] options = {"Close"};
                option = JOptionPane.showOptionDialog(rootPane, displayText,
                        "Filter Search Details", JOptionPane.OK_OPTION, JOptionPane.INFORMATION_MESSAGE, null,
                        options, options[0]);
            }
            if (option == 0) {
                // This stops the loop in case user clicked on close.
                break;
            } else {
                // Else continues the loop
                continue;
            }
        }
    }// GEN-LAST:event_filterBtnActionPerformed

