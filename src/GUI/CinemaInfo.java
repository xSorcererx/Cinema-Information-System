    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_addBtnActionPerformed
        int id;
        float price;
        boolean emptyRow = false;
        String idFldText, nameFldText, addressFldText, contactFldText, parkingText, threeDText, ticketPriceFldText;

        idFldText = idFld.getText();
        nameFldText = nameFld.getText();
        addressFldText = addressFld.getText();
        contactFldText = contactFld.getText();
        parkingText = parkingBtnGrp.getSelection().getActionCommand();
        threeDText = threeDBtnGrp.getSelection().getActionCommand();
        ticketPriceFldText = ticketPriceFld.getText();

        if ((idFldText == null || idFldText.isEmpty()) && (nameFldText == null || nameFldText.isEmpty())
                && (addressFldText == null || addressFldText.isEmpty())
                && (contactFldText == null || contactFldText.isEmpty())
                && (parkingText == null || parkingText.isEmpty()) && (threeDText == null || threeDText.isEmpty())
                && (ticketPriceFldText == null || ticketPriceFldText.isEmpty())) {
            JOptionPane.showMessageDialog(rootPane, "Please enter string or numerical value accoring to the field.",
                    "Null/Empty Value Found!", JOptionPane.ERROR_MESSAGE);
        }

        try {
            id = Integer.parseInt(idFld.getText());
            Long.parseLong(contactFldText);
            price = Float.parseFloat(ticketPriceFld.getText());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane,
                    "Cinema ID, contact and ticket price take numerical value. Please enter accordingly",
                    "Invalid Input Found!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Object[] data = {id, nameFld.getText(), addressFld.getText(), contactFld.getText(),
            parkingBtnGrp.getSelection().getActionCommand(), threeDBtnGrp.getSelection().getActionCommand(),
            price};
        DefaultTableModel newEntry = (DefaultTableModel) table.getModel();

        int rowCount = table.getRowCount();
        int colCount = table.getColumnCount();
        int nextRow = 0;
        String checkName;
        int checkID;

        if (rowCount == 0) {
            DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
            tableModel.addRow(data);
            JOptionPane.showMessageDialog(rootPane, data[1] + " cinema with ID " + data[0] + " has been added to the system.", "Cinema Added Successfully", JOptionPane.INFORMATION_MESSAGE);
        } else {
            do {
                checkID = (int) table.getValueAt(nextRow, 0);
                checkName = (String) table.getValueAt(nextRow, 1);
                if (checkName != null && checkName.length() != 0) {
                    if (data[0].equals(checkID) || data[1].equals(checkName)) {
                        JOptionPane.showMessageDialog(rootPane,
                                "Cinema ID and name must be unique. Please enter a unique value!",
                                "Duplicate Entry Found!", JOptionPane.ERROR_MESSAGE);
                        return;
                    } else {
                        nextRow++;
                    }
                } else {
                    emptyRow = true;
                }
            } while (nextRow < rowCount && !emptyRow);

            if (nextRow < rowCount) {
                for (int i = 0; i < data.length; i++) {
                    table.setValueAt(data[i], nextRow, i);
                }
                JOptionPane.showMessageDialog(rootPane, data[1] + " cinema with ID " + data[0] + " has been added to the system.", "Cinema Added Successfully", JOptionPane.INFORMATION_MESSAGE);
            } else {
                DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
                tableModel.addRow(data);
                JOptionPane.showMessageDialog(rootPane, data[1] + " cinema with ID " + data[0] + " has been added to the system.", "Cinema Added Successfully", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }// GEN-LAST:event_addBtnActionPerformed

